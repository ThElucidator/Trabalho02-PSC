    private void GetListarUsuarios() {
        DefaultTableModel d = (DefaultTableModel) grdUsuarios.getModel();
        
        //Limpar linhas
        while(d.getRowCount() > 0)
            d.removeRow(0);
        
        //Definir SQL
        this.c.setResultSet("SELECT CodUsuario,Nome,CPF,CodTipoAcesso FROM Usuario");
        
        //Mostrar resultado
        try {
            if (this.c.getResultSet().first()) {
                do {
                    d.addRow(
                       new Object[] {
                           this.c.getResultSet().getString("CodUsuario") ,
                           this.c.getResultSet().getString("Nome"),
                           this.c.getResultSet().getString("CPF"),
                           this.c.getResultSet().getString("CodTipoAcesso")
                       }
                    );                 
                } while(this.c.getResultSet().next());
            }
        }
        catch(SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

