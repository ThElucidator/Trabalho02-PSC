    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        if (txtDataConsulta.getText().equals("    -  -  ")) {
            JOptionPane.showMessageDialog(this, "Informar a Data da Consulta");
            txtDataConsulta.requestFocus();
        }
        else if (CBMedico.getSelectedItem()==null) {
            JOptionPane.showMessageDialog(this, "Selecione o Médico");
            CBMedico.requestFocus();
        }
        else if (CBPaciente.getSelectedItem()==null) {
            JOptionPane.showMessageDialog(this, "Selecione o Paciente");
            CBPaciente.requestFocus();
        }
        else {       
            String SQL;
            String Msg;
            
            //Cadastrar
            if (this.Consulta == -1) {
                Msg = "Registro adicionado com sucesso";
                SQL = "INSERT INTO Consulta (DataConsulta,CodMedico,CodPaciente,Observacoes) VALUES ('"+ txtDataConsulta.getText() +"','"+this.c.getCodigoComboBox(CBMedico,"Medico")+"','"+this.c.getCodigoComboBox(CBPaciente,"Paciente")+"','"+txtObserv.getText()+"')";
            }                                                                                                                       
            //Editar
            else {
                Msg = "Registro editado com sucesso";
                SQL = "UPDATE Consulta SET DataConsulta = '"+txtDataConsulta.getText()+"',CodMedico = '"+this.c.getCodigoComboBox(CBMedico, "Medico")+"',CodPaciente = '"+this.c.getCodigoComboBox(CBPaciente, "Paciente")+"',Observacoes = '"+txtObserv.getText()+"' WHERE Consulta = "+Consulta;                              
            }
            this.c.SQLExecute(SQL);
            JOptionPane.showMessageDialog(this, Msg);
            this.dispose();
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

