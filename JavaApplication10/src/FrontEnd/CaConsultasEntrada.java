    public CaConsultasEntrada(Integer Consulta) {
        initComponents();
        this.setModal(true);
        this.c = new Conexao();
        this.Consulta = Consulta;
        
        
    //Carregar combobox
        this.c.setComboBox(CBMedico, "Medico");
        this.c.setComboBox(CBPaciente, "Paciente");
    
    
    //Edição
        if (Consulta!=-1) {
            this.setTitle("Editar");
            btnCadastrar.setText("Editar");
            
            //Carregar os campos
            try {
                String SQL = " SELECT C.Consulta,C.DataConsulta,M.Nome,P.Nome,C.Observacoes FROM Consulta as C INNER JOIN Paciente as P ON C.CodPaciente = P.CodPaciente INNER JOIN Medico as M ON C.CodMedico = M.CodMedico WHERE C.Consulta = "+Consulta;
                this.c.setResultSet(SQL);
                this.c.getResultSet().first();
                txtDataConsulta.setText(this.c.getResultSet().getString("C.DataConsulta"));  
                CBMedico.setSelectedItem(this.c.getResultSet().getString("M.Nome"));
                CBPaciente.setSelectedItem(this.c.getResultSet().getString("P.Nome"));
                txtObserv.setText(this.c.getResultSet().getString("C.Observacoes"));
            }
            catch(SQLException e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }

